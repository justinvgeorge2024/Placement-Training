#include<stdio.h>

float addition(float a, float b){
    return a+b;
}
float substraction(float a, float b){
    return a-b;
}
float multiplication(float a, float b){
    return a*b;
}
float division(float a, float b){
    return a/b;
}
int modulo(int a, int b){
    return a%b;
}



